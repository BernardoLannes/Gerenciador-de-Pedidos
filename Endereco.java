public class Endereco {
    String rua;
    String bairro;
    String uf;
    int numero;
    int cep;

    public Endereco(String rua, String bairro, String uf, int numero, int cep) {
        this.rua = rua;
        this.bairro = bairro;
        this.uf = uf;
        this.numero = numero;
        this.cep = cep;
    }
}
